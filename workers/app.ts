import { createRequestHandler, type ServerBuild } from 'react-router';

// Import the compiled server build. `react-router build` emits it at
// build/server/index.js — the Worker bundle picks it up directly.
// This is the RR v7 replacement for Remix v2's `import * as build
// from '../build/server'` pattern that lived in the old Pages
// Function; the shape is compatible with `ServerBuild`.
//
// The eslint-disable + ts-ignore are because build/ is gitignored;
// tsc + eslint don't see the file until after `npm run build` runs.
// Also the no-restricted-imports rule would flag the relative parent
// import — legitimately, we can't reach `build/` through the `~/`
// alias since it lives outside `app/`.
//
// eslint-disable-next-line @typescript-eslint/ban-ts-comment
// @ts-ignore - build/server/index.js is generated by `react-router build`
// eslint-disable-next-line no-restricted-imports, import/no-unresolved -- generated at build time; outside `app/` so the `~/` alias doesn't reach it
import * as build from '../build/server';

// Cloudflare Workers + Static Assets runtime entrypoint. `wrangler.jsonc`
// sets `main` to this file and `run_worker_first: true`, so every request
// hits this Worker before Wrangler tries to serve a static asset.
//
// Because we intercept every request, we're responsible for delegating
// static-asset paths back to `env.ASSETS.fetch()`. Paths matching the
// asset prefixes below get handed off directly; everything else goes to
// the RR v7 request handler. This mirrors the Pages `_routes.json`
// exclusion list (which had `/assets/*`, `/fonts/*`, `/favicon.ico`,
// `/robots.txt`, `/sitemap.xml`, `/.well-known/*`).
const STATIC_PREFIXES = ['/assets/', '/fonts/', '/.well-known/'];
const STATIC_EXACT = new Set(['/favicon.ico', '/robots.txt', '/sitemap.xml']);

const requestHandler = createRequestHandler(build as unknown as ServerBuild);

export default {
  async fetch(request, env, ctx) {
    const url = new URL(request.url);
    if (STATIC_EXACT.has(url.pathname) || STATIC_PREFIXES.some((p) => url.pathname.startsWith(p))) {
      return env.ASSETS.fetch(request);
    }
    return requestHandler(request, { cloudflare: { env, ctx } });
  },
} satisfies ExportedHandler<Env>;
